    public enum DataType : uint
    {
        UnsignedByte = 1,      // 8-Bit-Wert ohne Vorzeichen (Unsigned Byte)
        AscciStr = 2,         // ASCII String Länge + 1 String ist nullterminiert
        UnsignedShort = 3,    // 16-Bit-Wert ohne Vorzeichen
        UnsignedInt = 4,       // 32-Bit-Wert ohne Vorzeichen(Unsigned Long)
        UnsignedRational = 5, // Bruch ohne Vorzeichen(Unsigned Rational) 8 4 Bytes Nenner, 4 Bytes Zähler
        SignedByte = 6,         // 8-Bit-Wert mit Vorzeichen(Signed Byte)
        Binaer = 7,           // Binär Beliebige Folge von Bytes
        SignedShort = 8,       // 16-Bit-Wert mit Vorzeichen
        SignedInt = 9,         // 32-Bit-Wert mit Vorzeichen (Signed Long)
        SignedRational = 10,  // Bruch mit Vorzeichen (Signed Rational   4 Bytes Nenner, 4 Bytes Zähler
        SignedFloat = 11,     // Gleitkommazahl einfach (Single Float) 4 Byte
        DoubleFloat = 12      // Gleitkommazahl doppelt(Double Float) 8 Byte
    }


